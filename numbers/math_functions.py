import math

print(math.pi)
print(math.e)
print(math.sin(2 * math.pi / 180))
print(math.sqrt(144))
print(math.floor(3.1235))               # next lower integer
print(math.trunc(-2.4321))              # drop decimal digits
print(pow(2,4), 2 ** 4, 2.0 ** 4.0)
print(abs(-42))
print(min(1,3,5,7))
print(max(1,2,3,4))
print(round(5.234), round(5.643))